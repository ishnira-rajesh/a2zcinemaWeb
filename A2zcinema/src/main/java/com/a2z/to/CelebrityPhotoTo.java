package com.a2z.to;

public class CelebrityPhotoTo {
	
	private long celebrityImageId;
	private String celebrityImageName;
	private String celebrityName;
	private byte[] imageData;

	public long getCelebrityImageId() {
		return celebrityImageId;
	}

	public void setCelebrityImageId(long celebrityImageId) {
		this.celebrityImageId = celebrityImageId;
	}

	public String getCelebrityImageName() {
		return celebrityImageName;
	}

	public void setCelebrityImageName(String celebrityImageName) {
		this.celebrityImageName = celebrityImageName;
	}

	public String getCelebrityName() {
		return celebrityName;
	}

	public void setCelebrityName(String celebrityName) {
		this.celebrityName = celebrityName;
	}

	public byte[] getImageData() {
		return imageData;
	}

	public void setImageData(byte[] imageData) {
		this.imageData = imageData;
	}
	
	

}
